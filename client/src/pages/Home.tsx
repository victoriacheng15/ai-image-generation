import { useHome } from "../hooks/useHome";
import Loader from "../components/Loader";
import FormField from "../components/FormField";
import RenderCard from "../components/RenderCard";

function Home() {
	const { loading, allPosts, searchText, searchedResults, handleSearchChange } =
		useHome();

	const title = (
		<h2 className="text-2xl md:text-3xl font-extrabold">The Community Showcase</h2>
	);
	
	const paragraph = (
		<p className="mt-2 text-base">
			Browse through a collection if imaginative and visually stunning images
			generated by DALLE-E AI
		</p>
	);

	return (
		<>
			{title}
			{paragraph}
			<div className="my-10">
				<FormField
					label="Search posts:"
					type="text"
					name="text"
					placeholder="Search something.."
					value={searchText}
					handleChange={handleSearchChange}
				/>
			</div>
			{loading ? (
				<Loader />
			) : (
				<>
					{searchText && (
						<h3 className="my-4 text-xl font-medium">
							Showing results for{" "}
							<span className="text-blue-800">{searchText}</span>{" "}
						</h3>
					)}
					<section className="grid gap-4 grid-cols-1 sm:grid-cols-3">
						{searchText ? (
							<RenderCard
								data={searchedResults}
								title="No search results found"
							/>
						) : (
							<RenderCard data={allPosts} title="No posts found" />
						)}
					</section>
				</>
			)}
		</>
	);
}

export default Home;
