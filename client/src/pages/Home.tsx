function RenderCard({ data, title }: { data: any[] | string; title: string }) {
	if (data?.length > 0) {
		// @ts-ignore
		return data.map((post) => <Card key={post._id} {...post} />);
	}

	return <h2 className="mt-5 text-xl font-bold text-blue-800">{title}</h2>;
}
